#!/bin/bash

echo -e "\033[1;36m========================================\033[0m"
echo -e "\033[1;36mОбновление stplr и пакетов из репозитория aides\033[0m"
echo -e "\033[1;36m========================================\033[0m"

ERROR=0

echo -e "\033[1;33m→ Обновление репозиториев stplr...\033[0m"
sudo stplr refresh
if [ $? -ne 0 ]; then
    echo -e "\033[1;31m❌ Ошибка: не удалось обновить репозитории stplr\033[0m"
    ERROR=1
else
    echo -e "\033[1;32m✓ Репозитории stplr обновлены\033[0m"
fi

echo -e "\033[1;33m→ Обновление пакетов stplr...\033[0m"
sudo stplr upgrade
if [ $? -ne 0 ]; then
    echo -e "\033[1;31m❌ Ошибка: не удалось обновить пакеты stplr\033[0m"
    ERROR=1
else
    echo -e "\033[1;32m✓ Пакеты stplr обновлены\033[0m"
fi

# Если ошибок не было - удаляем себя из очереди
if [ $ERROR -eq 0 ]; then
    rm -f "/tmp/alt-kde-helper-actions/$(basename "$0")"
    echo -e "\033[1;32m✅ Обновление stplr успешно завершено\033[0m"
else
    echo -e "\033[1;33m⚠ Были ошибки, скрипт останется в очереди для повторной попытки\033[0m"
fi
