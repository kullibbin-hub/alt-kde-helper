#!/bin/bash

echo -e "\033[1;36m========================================\033[0m"
echo -e "\033[1;36mОбновление Flatpak\033[0m"
echo -e "\033[1;36m========================================\033[0m"

ERROR=0

echo -e "\033[1;33m→ Обновление списка репозиториев Flatpak...\033[0m"
flatpak update --appstream
if [ $? -ne 0 ]; then
    echo -e "\033[1;31m❌ Ошибка: не удалось обновить списки репозиториев Flatpak\033[0m"
    ERROR=1
else
    echo -e "\033[1;32m✓ Списки репозиториев Flatpak обновлены\033[0m"
fi

echo -e "\033[1;33m→ Обновление Flatpak-приложений...\033[0m"
flatpak update -y
if [ $? -ne 0 ]; then
    echo -e "\033[1;31m❌ Ошибка: не удалось обновить Flatpak-приложения\033[0m"
    ERROR=1
else
    echo -e "\033[1;32m✓ Flatpak-приложения обновлены\033[0m"
fi

# Если ошибок не было - удаляем себя из очереди
if [ $ERROR -eq 0 ]; then
    rm -f "/tmp/alt-kde-helper-actions/$(basename "$0")"
    echo -e "\033[1;32m✅ Обновление Flatpak успешно завершено\033[0m"
else
    echo -e "\033[1;33m⚠ Были ошибки, скрипт останется в очереди для повторной попытки\033[0m"
fi
