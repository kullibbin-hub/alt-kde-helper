#!/bin/bash

echo -e "\033[1;36m========================================\033[0m"
echo -e "\033[1;36mОбновление системы\033[0m"
echo -e "\033[1;36m========================================\033[0m"

FLAG_FILE="/tmp/alt-kde-helper-needs-reboot"

# Удаляем старый флаг
rm -f "$FLAG_FILE"

echo -e "\033[1;33m→ Обновление списков пакетов (apt)...\033[0m"
sudo apt-get update
if [ $? -ne 0 ]; then
    echo -e "\033[1;31m❌ Ошибка: не удалось обновить списки пакетов\033[0m"
    exit 1
fi

echo -e "\033[1;33m→ Установка обновлений (apt)...\033[0m"

# Выполняем обновление и сохраняем вывод во временный файл
TEMP_LOG="/tmp/apt-dist-upgrade-output.$$"
sudo apt-get dist-upgrade -y 2>&1 | tee "$TEMP_LOG"

# Проверяем код возврата
if [ ${PIPESTATUS[0]} -ne 0 ]; then
    echo -e "\033[1;31m❌ Ошибка: не удалось установить обновления\033[0m"
    rm -f "$TEMP_LOG"
    exit 1
fi

# Анализируем вывод: ищем строки, указывающие на наличие обновлений
if grep -qE "^0 будет обновлено|^0 upgraded|^0 обновлено|Нет обновлений" "$TEMP_LOG"; then
    echo -e "\033[1;33m⚠ Обновлений не было. Перезагрузка не требуется.\033[0m"
else
    # Были обновления — создаём флаг
    touch "$FLAG_FILE"
    echo -e "\033[1;32m✅ Были установлены обновления. Флаг перезагрузки создан.\033[0m"
fi

# Удаляем временный файл
rm -f "$TEMP_LOG"

# Удаляем себя из очереди
rm -f "/tmp/alt-kde-helper-actions/$(basename "$0")"

echo -e "\033[1;32m✅ Обновление системы завершено успешно\033[0m"
